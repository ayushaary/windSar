labels = []

for i, (lat, lon, date) in enumerate(POINTS):
    try:
        # Get SAR patch
        sar = fetch_sar_image(lat, lon, date)
        np.save(f"data/patches/patch_{i:04d}.npy", sar["sar_array"])

        # Get ERA5 direction as label
        url = (f"https://archive-api.open-meteo.com/v1/archive"
               f"?latitude={lat}&longitude={lon}"
               f"&start_date={date}&end_date={date}"
               f"&hourly=wind_direction_10m")
        r = requests.get(url, timeout=15).json()
        direction = r['hourly']['wind_direction_10m'][12]  # noon
        labels.append(direction % 180)  # 0-180 for SAR ambiguity
        print(f"[{i}] lat={lat} lon={lon} → dir={direction:.1f}°")
    except Exception as e:
        print(f"[{i}] FAILED: {e}")
        labels.append(0.0)  # placeholder

np.save("data/labels.npy", np.array(labels, dtype=np.float32))
print("Dataset saved.")